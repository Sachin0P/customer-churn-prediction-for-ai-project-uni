import streamlit as st
import joblib
import pandas as pd

# -----------------------
# Load trained pipeline
# -----------------------
@st.cache_resource
def load_model():
    return joblib.load("customer_churn_random_forest.pkl")

model = load_model()

st.title("Customer Churn Prediction App")
st.write("Fill customer details to predict churn.")

# -----------------------
# User Inputs (ALL features)
# -----------------------

tenure = st.number_input("Tenure (months)", min_value=0, max_value=100, value=12)
monthly_charges = st.number_input("Monthly Charges", min_value=0.0, max_value=200.0, value=50.0)
total_charges = st.number_input("Total Charges", min_value=0.0, max_value=10000.0, value=600.0)

contract = st.selectbox(
    "Contract Type",
    ["Month-to-month", "One year", "Two year"]
)

internet_service = st.selectbox(
    "Internet Service",
    ["DSL", "Fiber optic", "No"]
)

payment_method = st.selectbox(
    "Payment Method",
    ["Electronic check", "Mailed check", "Bank transfer (automatic)", "Credit card (automatic)"]
)

online_security = st.selectbox("Online Security", ["Yes", "No"])
tech_support = st.selectbox("Tech Support", ["Yes", "No"])
paperless_billing = st.selectbox("Paperless Billing", ["Yes", "No"])

# -----------------------
# Prediction
# -----------------------

if st.button("Predict"):

    input_data = {
        "tenure": tenure,
        "MonthlyCharges": monthly_charges,
        "TotalCharges": total_charges,
        "Contract": contract,
        "InternetService": internet_service,
        "PaymentMethod": payment_method,
        "OnlineSecurity": online_security,
        "TechSupport": tech_support,
        "PaperlessBilling": paperless_billing
    }

    input_df = pd.DataFrame([input_data])

    prediction = model.predict(input_df)

    st.subheader("Prediction Result")

    if prediction[0] == 1:
        st.error("Customer is likely to CHURN")
    else:
        st.success("Customer is likely to STAY")

    if hasattr(model, "predict_proba"):
        probability = model.predict_proba(input_df)
        confidence = max(probability[0]) * 100
        st.write(f"Confidence: {confidence:.2f}%")
